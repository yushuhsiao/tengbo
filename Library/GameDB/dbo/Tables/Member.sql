CREATE TABLE [dbo].[Member] (
    [ID]          INT             NOT NULL,
    [CorpID]      INT             NOT NULL,
    [ACNT]        VARCHAR (20)    NOT NULL,
    [GroupID]     TINYINT         NOT NULL,
    [AgentID]     INT             NOT NULL,
    [Name]        NVARCHAR (20)   NOT NULL,
    [pwd]         VARCHAR (50)    NOT NULL,
    [sec_pwd]     VARCHAR (50)    NULL,
    [Locked]      TINYINT         NOT NULL,
    [Currency]    VARCHAR (3)     NOT NULL,
    [Balance]     DECIMAL (19, 6) CONSTRAINT [DF_Member_Balance] DEFAULT ((0)) NOT NULL,
    [BalanceMode] TINYINT         CONSTRAINT [DF_Member_BalanceMode] DEFAULT ((0)) NOT NULL,
    [Birthday]    DATETIME        NULL,
    [Tel]         VARCHAR (20)    NULL,
    [Mail]        VARCHAR (30)    NULL,
    [QQ]          VARCHAR (30)    NULL,
    [Memo]        NVARCHAR (100)  NULL,
    [Introducer]  VARCHAR (20)    NULL,
    [Sex]         TINYINT         NULL,
    [Addr]        NVARCHAR (100)  NULL,
    [UserMemo]    NVARCHAR (100)  NULL,
    [RegisterIP]  VARCHAR (20)    NULL,
    [CreateTime]  DATETIME        CONSTRAINT [DF_Member_CreateTime] DEFAULT (getdate()) NOT NULL,
    [CreateUser]  INT             NOT NULL,
    [ModifyTime]  DATETIME        CONSTRAINT [DF_Member_ModifyTime] DEFAULT (getdate()) NOT NULL,
    [ModifyUser]  INT             NOT NULL,
    CONSTRAINT [PK_Member] PRIMARY KEY CLUSTERED ([ID] ASC),
    CONSTRAINT [IX_Member] UNIQUE NONCLUSTERED ([CorpID] ASC, [ACNT] ASC)
);

